package performance;

public class Compute {

  public Integer computeOne(final Integer i) {
    return i + 1;
  }

  public Integer computeTwo(Integer i) {
    return i * 2;
  }

  public Integer computeThree(Integer i) {
    return i / 2;
  }

  public Integer computeFour(Integer i) {
    return i - 1;
  }

}
